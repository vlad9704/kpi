<div align="justify" style="margin-bottom: 30px;">
	<h6>
		<b>Кубашев Кумаргали Куаншалиевич</b>
	</h6>
	<img src="/upload/medialibrary/b35/100-_1_.jpg" class="img-right">
	<p style="text-indent: 30px;">
		В семье крестьянина Кубаша, проживавшего в селе Кызылбас Жанбайского сельского совета Новобогатского района Гурьевской области, в сентябре 1921 года на свет появился мой дед Кумаргали. Он был первенцем из сыновей. Детство было, как у многих его сверстников, голодное и рано был приучен к труду.  В 1939 году в Сорочинке закончил среднюю школу и поступил заочно в Гурьевское педагогическое училище. Дипломированных специалистов не хватало, и в сельской школе Кумаргали ата начал учить сельских детишек грамоте.
	</p>
	<p style="text-indent: 30px;">
		В 1942 году был призван в Красную Армию, и сразу был отправлен на фронт. Его боевые дороги пролегли через прибалтийские республики – Латвию, Литву и Эстонию. Здесь он сражался не только с немецко-фашистскими оккупантами, но и диверсантами. Кумаргали ата служил в артиллерийских войсках.  Данный род войск не зря называли "богом войны". В годы Великой Отечественной войны артиллеристы стали главным средством уничтожения не только живой силы врага, но и его ударных войск — танковых соединений.
	</p>
	<p style="text-indent: 30px;">
		Дважды был контужен из-за взорвавшихся вблизи немецких бомб. Несмотря на это он не уходил с поля боя, сражался с врагом. Медалью «За отвагу» был награжден 8 декабря 1943 года за то, что он в бою 1 декабря 1943 года за высоту 164.4 участвовал в отражении атаки противника и лично уничтожил из автомата 5 немецких солдат.
	</p>
	<p style="text-indent: 30px;">
		На войне вступил в ряды Всесоюзной Коммунистической партии большевиков.
	</p>
	<p style="text-indent: 30px;">
		В марте 1945 года по специальному направлению поступил в училище по подготовке офицеров артиллерийского миномета. В боевой характеристике, выданной заместителем командира войсковой части № 661 по артиллерии капитаном Вахнюктиным, отмечено следующее: «За период пребывания на фронте с 1942 года по 1945 год проявил себя дисциплинированным, стойким и храбрым умелым бойцом в борьбе с немецкими захватчиками. Под огнем противника не прекращал выполнять приказы своего командира, правильно и метко вел огонь из своего миномета по противнику, за что трижды награжден Правительственными наградами. Тов.Кубашев политичеки воспитан, морально устойчив и достоин быть офицером Красной Армии».  С этой характеристикой полностью был согласен командир артиллерийского полка, майор Соколов, о чем свидетельствует его подпись.
	</p>
	<p style="text-indent: 30px;">
		Боевой путь Кумаргали ата отмечен орденами «Красная Звезда», «Отечественная война 2-ой степени» и многими медалями.
	</p>
	<p style="text-indent: 30px;">
		Завершить учебу в училище Кумаргали ата не удалось, и он возвращается домой., и снова начинает  работать в школе. В феврале 1946 года уходит из жизни его отец, и забота о младших братьях школьного возраста легли на плечи Кумаргали ата и его сестры Рабиги. С 1946 по 1953 годы работал учителем, заведующим в школах населенных пунктов Уштаган, Жамбыл, Сарытобе. В 1953-1956 годах работал заместителем директора по хозяйственной части средней школы имени Карла Маркса в Джамбуле, председателем следственной комиссии колхоза «111 Интернационал», внештатным инспектором районного комитета Народного контроля. С марта 1956 года работал в колхозе «111 Интернационал», затем в совхозе «Забурун» бригадиром, экспедитором, управляющим фермой, заведующим центральным складом, вышел на пенсию в 1982 году.
	</p>
	<img src="/upload/medialibrary/d16/thumbnail_ata-soldat.jpg" class="img-left">
	<p style="text-indent: 30px;">
		В середине 70-х годов прошлого столетия ему предложили возглавить кооператив совхозных рабочих, на что он сказал: «Это ответственная работа. Я не боюсь даже тяжелой работы. Мне будет трудно потому, что это работа, где большие деньги и имущества. А там, где находятся деньги, не будет покоя. Не зря говорят, что там, где раньше идут деньги и политика, нет истины».  Он не захотел иметь неприятностей  и отказался от занимаемой должности.
	</p>
	<p style="text-indent: 30px;">
		Покойный Кумаргали ата вместо накопления имущества и славы,  предпочитал, чтобы его честь была незапятнанной и чтобы за спиной ничего плохого не говорили. В жизни он был требователен к себе и к окружающим.  Многие, кто знал Кумаргали ата с восхищением рассказывали о нем. 
	</p>
	<p style="text-indent: 30px;">
		После возвращения с войны Кумаргали ата женился на Фариде Ғазизқызы из рода Себек Бозай. У них родились 9 детей из них первенец Алпамыс умер в пять лет, а младший Ергали - умер в один год. Другие дети Кумаргали ата и Фариды аже получили хорошее воспитание и образование. Все обзавелись своими семьями. Кумаргали ата был главой большой семьи. В жизни ему повезло. Он сумел вернуться невредимым из страшной войны, пройти с честью все трудности военной и мирной жизни. Заслужить искренее уважение у народа. Внести свой вклад в образование и воспитание молодого поколения. Кумаргали ата прожил большую и счастливую жизнь. Умер в 1997 году. 
	</p>
	<p align="right">
		<b><i>Дархан ГУМАРОВ, руководитель службы административной работы</i></b>
	</p>
