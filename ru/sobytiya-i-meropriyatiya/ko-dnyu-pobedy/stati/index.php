<h3>
	<b>Дюсекенов Изим</b>
</h3>
<div align="justify" style="margin-bottom: 30px;">
	<img src="/upload/medialibrary/24a/Dyusekenov.jpg" class="img-right">
	<p style="text-indent: 30px;">
		<b>Я очень горжусь, что у меня был такой замечательный дедушка</b>
	</p>
	<p style="text-indent: 30px;">
		Великая Отечественная война! Как много горя принесла эта жестокая и беспощадная война. Сколько страданий легло на плечи нашего народа! Как много материнских слез было выплакано, как много крови было пролито. Все семьи Советского Союза пострадали в то грозное и жестокое время. Родители лишились своих дочерей и сыновей, жены – мужей, дети – родителей.
	</p>
	<p style="text-indent: 30px;">
		Молодые люди двадцать первого столетия о той страшной и беспощадной войне узнают из рассказов близких, книг, фильмов, телепередач. Мы живем в мирное время, но должны знать и помнить, каких неимоверных усилий стоило это мирное небо над нашей головой. Жизнь без войны нам подарили те парни и девушки, которые встретили её в те далёкие сороковые. Сейчас они – ветераны Великой Отечественной войны. Им досталась нелёгкая доля. К сожалению, очень мало осталось в живых ветеранов…
	</p>
	<p style="text-indent: 30px;">
		Я хочу рассказать о судьбе своего дедушки. Мой дед, Дюсекенов Изим Ажигалиевич родился 21 декабря 1924 года в селе Курсай Балыкшинского района Гурьевской области Казахской ССР. Окончил среднюю школу им. М.Ломоносова в Балыкшах.
	</p>
	<p style="text-indent: 30px;">
		<b>На фронт</b>
	</p>
	<p style="text-indent: 30px;">
		13 сентября 1942 года в возрасте восемнадцати лет был призван в ряды Красной Армии Гурьевским ГВК (городским военным комиссариатом). В сентябре 1942 года прибыл в воинскую часть: «Киров, ПЗБ -1231». С первых дней прибытия участвовал в боях против немецко-фашистских и японских войск по защите Социалистической Родины. Дошел до Берлина.  В течение 8 лет служил в рядах Советской Армии и Военно-Морского Флота.  Принимал участие в азовской флотилии в военно-морских операциях с немецко-фашистскими войсками. Также мой дедушка служил в составе Краснознаменной Амурской и Тихоокеанском флотилии.  Участвовал в наступательных военных операциях против Японии на самоходном пароходе ПЗБ 1231 (плавучая зенитная батарея), на штабном судне «Амур». 
	</p>
	<p style="text-indent: 30px;">
		<b>Самоходной пароход ПЗБ «плавучая зенитная батарея» - 1231</b>
	</p>
		<img src="/upload/medialibrary/69d/more.jpg" class="img-left">
	<p style="text-indent: 30px;">
		После окончания войны продолжал служить на различных судах Амурской флотилии. 28 января 1950 года на основании Указа Президиума Верховного Совета СССР уволен в запас.
	</p>
	<p style="text-indent: 30px;">
		Дюсекенов Изим Ажигалиевич награжден орденом Отечественной войны II степени, юбилейными медалями.
	</p>
	<p style="text-indent: 30px;">
		<b>Послевоенные годы</b>
	</p>
	<p style="text-indent: 30px;">
		Демобилизован в 1950 году, после чего в 1951 году создал семью женился на Арыстановой Алиме, на моей бабушке. У них родились 11 детей (восемь дочерей и трое сыновей). Моя мама Нагима Ескалиева (девичья фамилия Дюсекенова) вторая по счету. 
	</p>
	<p style="text-indent: 30px;">
		После войны мой дедушка Дюсекенов Изим поступил в КазНУ, получил юридическое образование. Позже окончил Джамбулский гидромелиоративно-строительный институт. После института работал в органах внутренних дел Макатского РОВД Гурьевском области Казахской ССР. Был начальником милиции Макатского РОВД. В разные годы работал:
		<ul>
			<li>инженером-строителем в Казахском Государственном научно-исследовательском и проектном институте «КазНИПИнефть» города Гурьева; главным инженером Строительно-монтажного управления «Трест ГурьевВодстрой»;</li>
			<li>начальником отдела кадров Аэрофлота города Гурьева; начальником ОКС Гурьевского завода местной промышленности; начальником отдела кадров Гурьевского Авторемонтного завода.</li>
		</ul>
	</p>
	<img src="/upload/medialibrary/b4b/ded.jpg" class="img-right">
	<p style="text-indent: 30px;">
		Для меня мой дед герой, как и все те, кто защищал нашу великую Родину.
	</p>
	<p style="text-indent: 30px;">
		Я очень горжусь, что у меня был такой замечательный дедушка.
	</p>
	<p style="text-indent: 30px;">
		Дюсекенов Изим Ажигалиевич умер 20 мая 1991 года, похоронен на кладбище Алгабас города Атырау.
	</p>
	<p style="text-indent: 30px;">
		Я верю в человеческую память, в умение любить и сострадать. Вечный огонь любви и памяти должен всегда гореть в наших сердцах и в сердцах будущих поколений как знак великой благодарности всем, кто отстоял мир и счастье на Земле. Я ощущаю себя крохотной частичкой нашего народа, а когда эти частички собираются вместе, то становятся единым сообществом, непобедимой силой, которой так боялись враги-захватчики.
	</p>
	<p style="text-indent: 30px;">
		Каждый из нас – наследник Победы, поэтому должен помнить о тех, кем завоёвана наша мирная жизнь!
	</p>
	<img src="/upload/medialibrary/db3/babushka.jpg" class="img-left">
	<p style="text-indent: 30px;">
		Многие сложили голову за нашу Великую Родину. Они погибли за то, чтобы жили мы, их потомки. Тем большая ответственность ложится на нас, молодое поколение. Мы должны доказать, что являемся достойными наследниками Победы, чтобы наши потомки могли гордиться нами так же, как мы гордимся славой своих предков.
	</p>
	<p style="text-indent: 30px;">
		Я хочу сказать нашим дорогим ветеранам: «Огромное спасибо за ваш подвиг!» и пожелать долголетия, здоровья и мирного неба над головой!
	</p>
	<p align="right">
		<b><i>Болатбек ЕСКАЛИЕВ, директор департамента по контролю строительства</i></b>
	</p>
