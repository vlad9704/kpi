<h3 align="center">
	<b>Бессмертный полк</b>
</h3>
<div align="justify" style="margin-bottom: 30px;">
	<img src="/upload/medialibrary/907/bessmertnyy-polk.jpg" class="img-right">
	<p style="text-indent: 30px;">
		На протяжении последних лет 9 Мая по центральным улицам городов многих стран проходит шествие бессмертного полка. Это движение в современном виде началось в 2011 году в Томске и его инициировали журналисты Сергей Лапенков, Сергей Колотовкин и Игорь Дмитриев. Они заметили, что всё меньше и меньше ветеранов принимает участие в уличных шествиях в День Победы, и решили восстановить справедливость: герои, отстоявшие свободу страны, должны видеть праздник – пусть даже с фотографий. В акции, получившей название «Бессмертный полк», в 2012 году приняло участие более шести тысяч человек. Эту акцию подхватили многие города и страны. Во всех городах Казахстана проходят такие шествия в День Победы или накануне празднования – 8 мая.
	</p>
	<p style="text-indent: 30px;">
		Наша семья впервые приняла участие в таком шествии в 2017 году в Москве. Мы сразу начали со столицы Российской Федерации – Москвы. В том году принимали участие более 800 тысяч человек. Вместе с нами прошли по Красной площади посланцы из республик постсоветского пространства, из Германии, Франции, Израиля и др.
	</p>
	<p style="text-indent: 30px;">
		До поездки нам говорили, что вас туда никто не пустит, надо пройти регистрацию и лишь после подтверждения сможете принять. Ничего этого не было.  Мы прошли к Тверской улице, пропустили нас через металлоискатель и влились в живую реку. Кругом звучала музыка военных лет, на ледэкранах показывали документальную хронику, военные художественные фильмы. Многие участники шествия были одеты в военную форму 40-х годов. Участвовали от мала до велика. Отцы несли на плечах своих маленьких детей. Окна домов, двери балконов были широко распахнуты и в них мы видели портреты участников войны. Их держали пожилые люди, видно, по состоянию здоровья они не могли пройти по улицам. Помню, как на одном балконе стояла пожилая женщина, вытирала на лице катившиеся слезы и держала большой портрет мужчины в военной форме. Вся грудь его была в орденах и медалях. Увидев ее скорбящую, слезы покатились из моих глаз. 9 Мая – это действительно праздник со слезами на глазах. 
	</p>
	<p style="text-indent: 30px;">
		Портреты своего родного деда Ораза Муканова и родного дяди Катена Габбасова нес мой муж Абдулла Габбасов. Его дед был призван в ряды действующей армии зимой 1942 года, ровно через год демобилизовали в связи с тяжелым ранением правой руки. По его рассказам,  в окоп попал немецкий снаряд и разорвался недалеко от него. Осколок попал в руку, раненным он продолжал участвовать в атаке, и лишь, когда обессилил от большой потери крови, попал в госпиталь, а потом его комиссовали. До войны дед работал учителем, но после ранения не мог держать мел в руке, писать на школьной доске, а левой рукой не получалось, он и поменял профессию. А Катен ага прошел все войну, закончил ее в Чехословакии. Когда освобождали Украину, он там встретил украинскую девушку и хотел жениться и привезти ее домой, но родители не разрешили. И он так и женился, прожил всю жизнь холостяком.
	</p>
	<p style="text-indent: 30px;">
		Портрет своего деда Жоламана Есенова нес его внук Маулен Габбасов. Дед Маулена, мой отец после краткосрочных курсов Гурьевского военно-пехотного училища, был направлен под Сталинград. После первого сражения от взвода, как вспоминал он, осталось два человека. «Это была мясорубка», - говорил отец. Потом было ранение, госпиталь. Отца, как грамотного человека, знающего казахский и русский языки, имеющего образование (педагогическое и военно-пехотное училища) потом направили в Оренбург для участия в подготовке младщих сержантов. В ноябре 1946 года демобилизовался. Был награжден орденом Отечественной войны.
	</p>
	<p style="text-indent: 30px;">
		Надежда Грицюк – супруга моего сына несла портрет прославленного героя, своего деда Ивана Петровича Гаврилейченко. Ему было 16 лет, когда его родное село заняли фашисты. Подростков, молодежь фашисты начали угонять в Германию, в рабство. После того, как их загрузили в товарняк, мысли о свободе не давали покоя Ивану. И он совершил побег из товарного поезда с группой смельчаков. Они разобрали пол, и ночью, через небольшое отверстие смогли спуститься на шпалы под движущимся поездом. Младший его брат побоялся и был угнан, вернулся на Родину после окончания войны. А Иван  с товарищами в лесах нашел партизан, и два года партизанил. Потом он воевал в действующей Красной Армии. Освобождал Польшу, брал Берлин. В Берлин он вошел Победителем, а не рабом, как хотели в 41-м  немецко-фашистские захватчики. Домой он вернулся в 1949 году, служил в Германии. Его боевой путь отмечен двумя орденами Славы 2-ой и 3-ей степени, орденом Отечественной войны, медалями «За отвагу», «За освобождение Варшавы», «За взятие Берлина». После войны строил Волго-Донский канал, потом работал в Кунусе, и в 1954 году судьба его забросила в Казахстан, участвовал в освоении целины, и за это тоже имел награды. Здесь он создал семью, здесь он обрел вторую родину.
	</p>
	<p style="text-indent: 30px;">
		Я несла портрет своего родного дяди Аманжола Султангалиева. Он старший брат моего отца, носил фамилию своего деда. Весной 1941 года был призван на службу, проходил ее в Белоруссии. А потом отступал до Москвы. 12 декабря 1941 года его не стало. Погиб, защищая Москву. Ему тогда было неполных 20 лет.
	</p>
	<p style="text-indent: 30px;">
		Мы шли под флагом Казахстана, и к нам примкнули бывшие наши соотечественники, из других городов страны.
	</p>
	<p style="text-indent: 30px;">
		Уже давно наши прославленные отцы, деды ушли в мир иной, но мы о них помним. И пока мы будем помнить их, вспоминать, они живы. В этом году, в год 75-летия Великой Победы, мы вновь решили принять участие в шествии на Красной Площади. Начали готовиться с января, но сегодняшние реалии таковы, что уже никуда не едем.
	</p>
	<p style="text-indent: 30px;">
		Будем жить, будем надеяться на лучшее!
	</p>
	<p align="right">
		<b><i>Елена ЕСЕНОВА</i></b>
