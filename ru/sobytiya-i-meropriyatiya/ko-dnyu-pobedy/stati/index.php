<div align="justify" style="margin-bottom: 30px;">
	<p>
		<b><i>Для многих поколений, живущих на постсоветском пространстве, 9 мая был и остается святым праздником. Время не может стереть память об узниках концлагерей, сожженных городах и деревнях, блокаде Ленинграда, кровопролитных боях за Брест, Москву, Минск, Киев и многих других городов.  27 миллионов советских людей отдали свою жизнь в борьбе с фашизмом. Они шагнули в бессмертие, встав на защиту своей Родины. Об их стойкости, мужестве, героизме написаны тысячи книг, сняты сотни фильмов, открыты музеи, и продолжаются поиски. История Великой Отечественной войны пополняется новыми материалами, фактами. И не угасают споры между когда-то воюющими коалициями, союзниками.</i></b>
	</p>
	<p>
		<b><i>Война в 41-м году вошла в каждую советскую семью. Одни уходили в действующую армию, другие самоотверженно трудились на заводах, фабриках, в колхозах. Победа ковалась на фронте и в тылу.</i></b>
	</p>
	<h6>
		<b>Михаил и Валентина Улитины</b>
	</h6>
	<img src="/upload/medialibrary/1df/foto-Mikhaila-Ulitinavv.jpg" class="img-right">
	<p style="text-indent: 30px;">
		О фронтовой жизни Михаила Улитина вспоминают часто. За свою боевую биографию гвардии младший сержант Михаил Улитин повидал много. Он прошел всю войну. Был призван в 1941 году Гурьевским военкоматом и направлен в 212-й Гвардейский гаубичный артиллерийский полк 8-ой ГГА Бригады Резерва Главного Командования.
	</p>
	<p style="text-indent: 30px;">
		<i><b>Спасение ребенка</b></i>
	</p>
	<p style="text-indent: 30px;">
		Боевых эпизодов было множество, но особенно запомнился случай, произошедший с ним в 1942 году. Шли ожесточенные на Юго-Западном фронте. Под Белгородом Михаил Улитин вместе с другими бойцами оказался в окружении. Выбираясь из окружения, они вышли к небольшой деревеньке. В одном из домов, где проживала молодая женщина с грудным ребенком, Михаил и его товарищ попросили укрытия. Женщина, недолго думая, спрятала их за печкой и прикрыла соломой. Минутой спустя в дом ворвался немец. Увидев женщину, бросился на нее, пытаясь ее изнасиловать. Тут заплакал малыш, и фашист, отвлекшись от женщины, наставил автомат на ребенка. Михаил, забыв о собственной безопасности, выбрался из укрытия и застрелил врага.
	</p>
	<p style="text-indent: 30px;">
		Потом были долгие скитания по лесу в поисках своей части. Попал в плен, бежал, смог выжить.
	</p>
	<p style="text-indent: 30px;">
		После окончания войны, вернувшись в свой родной город, Михаил Иванович отыскал ту семью. И однажды семья Бородиных приехала в Гурьев, чтобы навестить Улитиных. Из воспоминаний Валентины Улитиной: «Чета Бородиных (Михаил и Сима) привезли с собой детей: дочку Любу, которую спас муж и маленького сыночка, родившегося после войны. Вспоминая то нелегкое время, долго плакали».
	</p>
	<p style="text-indent: 30px;">
		«Гордитесь своим отцом, он очень смелый и самоотверженный человек», сказал тогда Михаил Бородин, - вспоминает старшая дочь Улитиных Надежда, - и, хотя мы были тогда совсем маленькими, все же понимали, что наш отец – герой».
	</p>
	<p style="text-indent: 30px;">
		Семьи долго переписывались. Люба Бородина окончила Ленинградский мединститут и впоследствии стала профессором медицины. 
	</p>
	<p style="text-indent: 30px;">
		<i><b>Фронтовые дороги</b></i>
	</p>
	<p style="text-indent: 30px;">
		В боевых действиях Михаил Улитин принимал участие с 1941 года. Был награжден боевым орденом «Красная Звезда», медалями «За боевые заслуги», «За победу над Германией». Участвовал в штурме Берлина.  9 мая 1945 года для Михаила Улитина не закончилась война, его часть направили на Дальний Восток, где Советский Союз готовился к боевым действиям с Японией. И с августа уже принимал участие в разгроме японской армии. Фронтовые дороги Михаила Улитина пролегли через Монголию, Северную Корею.
	</p>
	<p style="text-indent: 30px;">
		Из представления №035452 (1944 г.), подписанным командиром, майором 212-ого полка Юрченко: «Во время прорыва обороны противника в районе Бураки-Осиповка и преследования противника в направлении Сенно первая колонна, 500 метров восточнее Сенно, подверглась бомбежке с воздуха. Прямым попаданием загорелись две рядом стоящие машины и цистерны с горючим. Пламя угрожало распространению очага пожара на рядом стоящие машины. Гвардии младший сержант Улитин, пренебрегая жизнью, с плащпалаткой в руках, несмотря на горевшееся личное обмундирование, вместе с шофером Калашниковым ликвидировали пожар трех автомашин и тем самым не допустили выход из строя. При вторичном заходе «мессеров» гвардии младший сержант Улитин, несмотря на пулеметный обстрел, завел с трудом заводившуюся штабную машину и вывел ее из обстрела, предварительно положив на нее двух раненных офицеров, которым фельдшером пехоты была оказана помощь.
	</p>
	<p style="text-indent: 30px;">
		Представляем гвардии младшего сержанта Улитина, шофера штабной машины к правительственной награде – ордену «Красная Звезда».
	</p>
	<p style="text-indent: 30px;">
		23 июля 1944 года от имени Президиума Верховного Совета СССР за образцовое выполнение заданий командования на фронте борьбы с немецкими захватчиками и проявленные при этом доблесть и мужество Михаил Улитин был награжден орденом «Красная Звезда».
	</p>
	<p style="text-indent: 30px;">
		И немного позже за особую храбрость и мужество при защите Отечества был награжден орденом Красного Знамени. С двумя орденами и воинской медалью «За боевые заслуги» на груди глубокой осенью 1945 года Михаил Улитин вернулся домой. 
	</p>
	<p style="text-indent: 30px;">
		Как вспоминает его родные: «Дедушка рассказывал, что в Берлин советские солдаты входили уставшими, исхудавшими, и он тоже был в таком состоянии. Наши воины держались, чтобы добить фашиста в его логове. У него самого не было даже носков, портянок. Надевал кирзовые сапоги на голые ноги, натирая их до крови. Искал в разрушенных домах хоть кусочек материи, чтобы обмотать израненные ноги, а нашел заштопанные чистые немецкие носки. Никаких трофеев дедушка с войны не привез. Как ушел в одной гимнастерке, так в гимнастерке вернулся, в ней и женился. Еще много трудностей было и у дедушки и бабушки в первые годы после войны. Но так жил весь народ».
	</p>
	<p style="text-indent: 30px;">
		Когда Михаил Улитин воевал на фронте, его будущая жена Валентина работала радисткой.
	</p>
	<p style="text-indent: 30px;">
		<i><b>Радиостанция под землей</b></i>
	</p>
	<p style="text-indent: 30px;">
		До начала войны Валентина Горина с отличием окончила Гурьевский политехникум и получив специальность радиооператора, стала работать на центральном телеграфе. Сообщение о нападении Германии на Советский Союз она получила по аппарату, за которым работала. Когда был объявлен приказ о всеобщей мобилизации, Валентина попросилась добровольцем на фронт. Но ей было отказано, и она была направлена радисткой в специальное подразделение воздушного наблюдения, оповещения и связи г. Гурьев. В подземном бункере в одной из зданий старого города стала проходить служба Валентины Гориной.
	</p>
	<p style="text-indent: 30px;">
		- В этом бункере была расположена радиостанция, по которой мы отслеживали маршруты вражеских самолетов, а летали они совсем близко, поскольку Сталинград, где шли ожесточенные бои, был рядом. Подразделение было засекреченным, о том, что я там работаю, не знали даже мои близкие родственники, - вспоминала Валентина Михайловна.
	</p>
	<p style="text-indent: 30px;">
		О своей работе «под землей» рассказывала неохотно.
	</p>
	<p style="text-indent: 30px;">
		- Мы давали присягу о неразглашении военной тайны, и даже после происшествия стольких лет, я не могу их выдать, - говорила Валентина Михайловна. 
	</p>
	<p style="text-indent: 30px;">
		Но даже из скупых воспоминаний бабы Вали, как называли ее внуки, работа ее была напряженной и ответственной. Дежурство там велось круглосуточно, радистки не спали ночами, отслеживая координаты вражеских самолетов. В одно из ее дежурств в небе вблизи Гурьева появился иностранный самолет, может, случайно залетел. Переполох был, но большого ЧП не случилось. 
	</p>
	<p style="text-indent: 30px;">
		Министерство Обороны СССР высоко оценило наработанный за годы войны профессиональный опыт Валентины, и после окончания войны ей предложили переехать в Москву в распоряжение противовоздушных войск, но она не могла оставить больную мать и маленького братишку.
	</p>
	<p style="text-indent: 30px;">
		- Для меня, конечно, было большой честью получить приглашение от самого главнокомандующего сил противовоздушной обороны страны, но по-другому я поступить не могла, - с грустью вспоминала Валентина Михайловна, - Жизнь после войны была очень трудная. Нужно было как-то выживать. Я была самой старшей в семье, поэтому все заботы о семье легли на мои плечи. Да и тяжело было уехать из родного города, где я родилась, училась. Поэтому отказалась. Иногда жалею, что не поехала в Москву, моя судьба могла быть совсем другой.
	</p>
	<p style="text-indent: 30px;">
		В послевоенные годы профессия радистки была востребованной, и еще долгие годы она проработала в секретном подразделении Гурьева.
	</p>
	<p style="text-indent: 30px;">
		<i><b>Семья</b></i>
	</p>
	<p style="text-indent: 30px;">
		После войны в соседский дом вернулся Михаил Улитин, Валентина помнила его мальчишкой. Домой он вернулся опаленным войны фронтовиком. Симпатичная девушка Валя приглянулась Михаилу, и они решили пожениться. У них родились две дочери и сын. 
	</p>
	<img src="/upload/medialibrary/0fb/14141.jpg" class="img-left">	
	<p style="text-indent: 30px;">
		Долгие годы Валентина Михайловна проработала в Западно-Казахстанской экспедиции геофизических исследований скважин и Прикаспийской нефтеразведочной экспедиции объединения «Гурьевнефтегазгеология». Сам Михаил Иванович Улитин работал главным механиком, но в какой организации, к сожаленью, дети не помнят.  
	</p>
	<p style="text-indent: 30px;">
