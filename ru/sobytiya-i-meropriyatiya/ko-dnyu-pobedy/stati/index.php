	</p>
	<p style="text-indent: 30px;">
		Полк, в котором служил мой дед, стал продвигаться на Запад. Принимал участие в освобождении Пскова, эстонских городов Пяриу, Тарту и Таллина. С боями, отвоевывая у немецко-фашистских захватчиков каждую пядь советской земли, шли через карельские леса. В марте 1944 года Шанов Орынбасар был награжден медалью «За воинскую доблесть». В июле 1944 года принимал участие в освобождении прибалтийских республик. В феврале 1945 года военный эшелон прибыл в Польшу, и мой дед освобождал польские города, страны Восточной Европы.  В составе 1-го Белорусского фронта под командованием маршала Георгия Жукова участвовал во взятии Берлина. 2 мая был в Берлине и окончание войны встретил там. 
	</p>
	<img src="/upload/medialibrary/5fd/IMG_20200428_WA0001.jpg" class="img-left">
	<p style="text-indent: 30px;">
