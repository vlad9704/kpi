	</p>
	<p style="text-indent: 30px;">
		Мирная жизнь моего деда началась в родном колхозе. Женился, и вместе с моей бабушкой родили и воспитали 10 детей. Всех вывели в большую жизнь, дав достойное воспитание и образование.  С 1954 по 1987 годы трудился в сфере торговли. Был награжден нагрудным знаком «Отличник торговли». В 2008 году, в возрасте 83-х лет, мой дед ушел из жизни. 
	</p>
	<p style="text-indent: 30px;">
		Горжусь, что мой дед – герой ушедшей войны и я – внук Победителя!
