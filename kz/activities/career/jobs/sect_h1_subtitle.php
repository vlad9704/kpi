<p>
	"KPI Inc ." ЖШС жоғары білікті және тәжірибелі мамандарды, сондай-ақ ұзақ мерзімді қарым-қатынасты құруға бағытталған дарынды түлектерді тартуға ерекше көңіл бөледі.
</p>
<p>
	Біздің корпоративтік құндылықтарымызды бөлісуге және "KPI Inc." ЖШС дамуына өз күшін, білімін, қабілеті мен талпынысын салуға дайын адамдарды жұмысқа шақырамыз.
</p>
<p>
	Сіздің назарыңызға «KPI Inc.» ЖШС-дегі өзекті бос жұмыс орындарын ұсынамыз.
</p>