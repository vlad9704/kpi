	<li>Өрістерді толтырыңыз.</li>
	<li>“Өтінішті жіберу " түймешігін басыңыз.</li>
	<li>Сіздің ұялы телефоныңызға растау коды бар СМС жіберіледі.</li>
	<li>Кодты енгізіңіз және "Растау" түймешігін басыңыз.</li>
</ol>
