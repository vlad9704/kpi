	</p>
	<p style="text-indent: 30px;">
		После окончания войны, вернувшись в свой родной город, Михаил Иванович отыскал ту семью. И однажды семья Бородиных приехала в Гурьев, чтобы навестить Улитиных. Из воспоминаний Валентины Улитиной: «Чета Бородиных (Михаил и Сима) привезли с собой детей: дочку Любу, которую спас муж и маленького сыночка, родившегося после войны. Вспоминая то нелегкое время, долго плакали».
	</p>
