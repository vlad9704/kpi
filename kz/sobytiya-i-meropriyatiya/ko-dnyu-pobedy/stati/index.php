		<b>Михаил и Валентина Улитины</b>
	</p>
	<img src="/upload/medialibrary/1df/foto-Mikhaila-Ulitinavv.jpg" class="img-right">
	<p style="text-indent: 30px;">
		О фронтовой жизни Михаила Улитина вспоминают часто. За свою боевую биографию гвардии младший сержант Михаил Улитин повидал много. Он прошел всю войну. Был призван в 1941 году Гурьевским военкоматом и направлен в 212-й Гвардейский гаубичный артиллерийский полк 8-ой ГГА Бригады Резерва Главного Командования.
