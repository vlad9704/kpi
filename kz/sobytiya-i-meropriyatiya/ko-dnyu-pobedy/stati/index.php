		«Гордитесь своим отцом, он очень смелый и самоотверженный человек», сказал тогда Михаил Бородин, - вспоминает старшая дочь Улитиных Надежда, - и, хотя мы были тогда совсем маленькими, все же понимали, что наш отец – герой».
	</p>
	<p style="text-indent: 30px;">
		Семьи долго переписывались. Люба Бородина окончила Ленинградский мединститут и впоследствии стала профессором медицины. 
